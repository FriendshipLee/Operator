
public class Example03 {

	public static void main(String[] args) {
		int i = 3;
		System.out.println(i);
		
		//대입연산자 기준으로 오른쪽에 있는 값을 왼쪽에 대입
		i += 2;
//		i = i + 2;
		System.out.println(i);
		i -= 2;
//		i = i - 2;
		System.out.println(i);
		i *= 2;
//		i = i * 2;
		System.out.println(i);
		i /= 3;
//		i = i / 3;
		System.out.println(i);
		i %= 2;
//		i = i % 2;
		System.out.println(i);
		
	}

}
